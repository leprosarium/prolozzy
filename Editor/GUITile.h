class cGUITileMap : public cGUIItem
{
	int m_mode;										// 0=none, 1=selecting, 2=move selection
	iV2 m_move;										// grab point in selection 
	int	Snap(int x) { return snap ? (x/8)*8 + (x%8>=4)*8 : x; }				// snap value to grid
public:
	int scale;
	bool snap;
	bool grid;
	bool axes;
	iRect map;

	cGUITileMap();

	virtual	void OnUpdate();
	virtual	void OnDraw();
};


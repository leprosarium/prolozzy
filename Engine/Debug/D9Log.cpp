void log::openChannels(bool opn)
{
	_open = opn;
	if(open()) 
		for(channel & ch: channels)
			ch.add(&file).add(&sys);
	else
	{
		get(Channel::sys).add(&file).add(&sys);
		get(Channel::err).add(&file).add(&sys);
	}
}

